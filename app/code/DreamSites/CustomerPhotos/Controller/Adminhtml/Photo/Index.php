<?php
/**
 * Admin Photo Listing Controller
 */
namespace DreamSites\CustomerPhotos\Controller\Adminhtml\Photo;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;

class Index extends Action
{
    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * @param Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * Check ACL
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('DreamSites_CustomerPhotos::photo');
    }

    /**
     * Display customer photos grid
     *
     * @return \Magento\Framework\View\Result\Page
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('DreamSites_CustomerPhotos::photo');
        $resultPage->getConfig()->getTitle()->prepend(__('Customer Photos'));
        return $resultPage;
    }
}
